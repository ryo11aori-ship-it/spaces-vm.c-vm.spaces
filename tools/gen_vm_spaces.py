import sys

def build_pure_vm():
    # ==========================================================
    # The God-Slayer: Pure Spaces Meta-Circular Evaluator
    # ==========================================================
    # ASCII文字('+','-')を経由せず、直接3ビット空白を0〜7にデコードし、
    # インターリーブテープ上で直接ジャンプ・実行を行う究極のVM。
    
    bf_code = """
    # 1. ロードフェーズ: 3ビットデコードとメモリ展開
    >+++++[<-------->-]<
    [
        >[->+>+<<]>[-<+>]>[<+>-]
        <<[->>+<<]
        >>[>]<[<]>[>+>+<<-]>>[<<+>>-]<[>+<-]>>>
    ]
    # 2. 実行フェーズ: メタインタプリタのメインループ
    >[
        >[-<+>>+<]>[-<+>]
        <<[->>+<<]
        # オペコード(0~7)のディスパッチと長距離ポインタ移動(IP/DP)
        >[->+<]>[-<+>]
        <<[<]>[[>]>>[>>]+[<<]<[<]<+>>-]>[>]+[->>]
        <<<<[[<<]<[<]+<<[+>+<<-[>-->+<<-[>+<[>>+<<-]]]>[<+>-]<]++>>-->[>]>>[>>]]
        <<[>>+<[[<]<]>[[<<]<[<]+[-<+>>-[<<+>++>-[<->[<<+>>-]]]<[>+<-]>]>[>]>]
        >[>>]>>
        # データの入出力と演算
        <<[>>+>>+>>]<<[->>>>>>>>]<<[>.>>>>>>>]<<[>->>>>>]<<[>,>>>]<<[>+>]<<[+<<]<
    ]
    """
    # コメントと空白を排除して純粋なBFコードとしてエミット
    clean_code = "".join(c for c in bf_code if c in "><+-.,[]")
    sys.stdout.write(clean_code)

if __name__ == "__main__":
    build_pure_vm()

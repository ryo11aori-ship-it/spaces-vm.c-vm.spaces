def build_pure_vm():
    dbfi_prefix = ">>>+[[-]>>[-]++>+>+++++++[<++++>>++<-]++>>+>+>+++++[>++>++++++<<-]+>>>"
    dbfi_suffix = "<++[[>[->>]<[>>]<<-]<[<]<+>>[>]>[<+>-[<+>-]>>]<<-]<<<[->>+<<][<<+>+>-[<+>>+<->]<[>+<-]>-[<+>-]<[>+<-]<[>+<-]>[[-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<-]>[[-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<+>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<<[<]<+>>[>]>]>[[-]<<[<]>-<+>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]<[[-]<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-]>>[>]>]>[[-]<[<]<[[-]>+<<->+<<->+<<->+<<->+<<->+<<->+<<->+<<-]>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]>.>>[>]>]>[[-]<[<]>,>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]<+>>[>]>]>[[-]<<[<]>-<+>>[>]>]<<<[<]>]<<[<]>[[-]<[<]>>>[<]<<[<]>]<<[<]>]"

    map_code = ""
    for val, ascii_code in enumerate([62, 60, 43, 45, 46, 44, 91, 93]):
        map_code += "[->+>>+<<<]>>>[-<<<+>>>]<<"
        map_code += "-" * val
        map_code += ">+<"
        map_code += "[>-<[->>+<<]]"
        map_code += ">[-"
        map_code += "<<<" + ("+" * ascii_code) + ">>>"
        map_code += "]<"
        map_code += ">>[-]<<"

    macro = ""
    macro += ">[-]<"
    macro += ">>,"
    macro += ">+<"
    macro += "["
    macro += ">-<"
    macro += "++++++++[<---->-]<"
    macro += ">>[-]<<"
    macro += "["
    macro += ">>+<<"
    macro += ",,"
    macro += "[-]"
    macro += "]"
    macro += ">>[-<<<+>>>]<<"

    macro += "<"

import sys

def build_pure_vm():
    # DBFI (Daniel B. Cristofani's Brainfuck Interpreter) のセットアップコード
    dbfi_prefix = ">>>+[[-]>>[-]++>+>+++++++[<++++>>++<-]++>>+>+>+++++[>++>++++++<<-]+>>>"
    
    # DBFIの実行エンジン本体
    dbfi_suffix = "<++[[>[->>]<[>>]<<-]<[<]<+>>[>]>[<+>-[<+>-]>>]<<-]<<<[->>+<<][<<+>+>-[<+>>+<->]<[>+<-]>-[<+>-]<[>+<-]<[>+<-]>[[-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<-]>[[-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<[>+<-]>>[<<+>>-]<+>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<<[<]<+>>[>]>]>[[-]<<[<]>-<+>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]<[[-]<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-<+>>-]>>[>]>]>[[-]<[<]<[[-]>+<<->+<<->+<<->+<<->+<<->+<<->+<<->+<<-]>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]>.>>[>]>]>[[-]<[<]>,>>[>]>]<<<[<]>]<<[<]>[-[+>>+<->]<[>+<-]>-[<+>-]<[>+<-]>[[-]<[<]<+>>[>]>]>[[-]<<[<]>-<+>>[>]>]<<<[<]>]<<[<]>[[-]<[<]>>>[<]<<[<]>]<<[<]>]"

    # 3ビット空白デコーダー & ASCIIマッピング・ブートローダー
    macro = ""
    macro += ">[-]>[-]>[-]>[-]<<<<\n" # 一時変数の初期化
    macro += ">, \n" # 最初の1文字目を読み込む
    macro += "[\n"   # 【修正点】もし EOF (0) でなければデコード開始！
    
    # 1ビット目
    macro += "  >+< [ >-< ++++++++[<---->-]< >>[-]<< [ >>+<< ,, [-] ] >>[-<<<+>>>]<< ]\n"
    macro += "  < [->+<]>[-<++>]< \n" # 左シフト(2倍)
    
    # 2ビット目
    macro += "  >\n"
    macro += "  , \n"
    macro += "  >+< [ >-< ++++++++[<---->-]< >>[-]<< [ >>+<< ,, [-] ] >>[-<<<+>>>]<< ]\n"
    macro += "  < [->+<]>[-<++>]< \n" # 左シフト(2倍)
    
    # 3ビット目
    macro += "  >\n"
    macro += "  , \n"
    macro += "  >+< [ >-< ++++++++[<---->-]< >>[-]<< [ >>+<< ,, [-] ] >>[-<<<+>>>]<< ]\n"
    
    # 構築された値(0-7)をASCIIに変換して出力セルへ
    macro += "  < \n"
    macro += "  [->+<] \n"
    macro += "  > \n"
    for val, ascii_code in enumerate([62, 60, 43, 45, 46, 44, 91, 93]):
        macro += "[->+>+<<]>>[-<<+>>]<" # C を T1 にコピー
        macro += "-" * val
        macro += ">+<" # フラグを立てる
        macro += "[>-<[-]]" # 一致しなければフラグを折る
        macro += ">[-" # 一致した場合のみ実行
        macro += "<<<<" + ("+" * ascii_code) + ">>>>" # ASCII文字を対象セルに書き込む
        macro += "]<"
        macro += "[-]<" # 後片付け
    macro += "  [-] \n" # ループの最後で自身を0にする
    macro += "]\n"
    macro += "<\n" # 対象セルに戻る (EOFなら0のまま)

    # ブートローダーとエンジンを結合して出力
    sys.stdout.write(dbfi_prefix + macro + dbfi_suffix)

if __name__ == "__main__":
    build_pure_vm()
